/*Write a Java program to get a number whether it is positive or negative.*/

class PosNeg{

public static void main(String args[]){

int valCheck = -5;

if (valCheck > 0) {
System.out.println("This value is positive");
	}


else if (valCheck < 0) {
System.out.println("This value is negative");
	}
else{
System.out.println("This value is 0");
	}
    }
}
